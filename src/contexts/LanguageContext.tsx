const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>(() => {
    const stored = localStorage.getItem('wifiric-language');
    if (stored) return stored as Language;
    
    // Auto-detect browser language
    const browserLang = navigator.language.split('-')[0].toLowerCase();
    const supportedLanguages: Language[] = ['fr', 'en', 'es', 'de', 'pt'];
    
    if (supportedLanguages.includes(browserLang as Language)) {
      return browserLang as Language;
    }
    
    return 'fr'; // Default fallback
  });
  const [isTransitioning, setIsTransitioning] = useState(false);

  const setLanguage = useCallback((lang: Language) => {
    if (lang === language) return;
    
    setIsTransitioning(true);
    setLanguageState(lang);
    localStorage.setItem('wifiric-language', lang);
    
    // Quick transition - just enough to be smooth
    setTimeout(() => {
      setIsTransitioning(false);
    }, 150);
  }, [language]);

  const t = useCallback((key: string): string => {
    return translations[language][key] || key;
  }, [language]);

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, isTransitioning }}>
      <div className={`transition-all duration-150 ease-in-out ${isTransitioning ? 'opacity-95 blur-[1px]' : 'opacity-100 blur-0'}`}>
        {children}
      </div>
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};
